#!/usr/bin/env python3
"""HF-aware models.dev full-catalog seed (OFFLINE).

The generator logic in `scripts/refresh_from_modelsdev.py` produces the FULL
models.dev catalog (group -> mint -> alias, ~1,300 entries). But the HF-oracle
seed pass already minted every HF-present model as a canonical with HF-true
casing (in `seed/models/sources/hf_oracle.generated.yaml` + the re-cased entries
inside `seed/models/sources/models_dev.generated.yaml`). HF source-of-truth WINS
id + casing — so we must NOT re-mint a lowercase duplicate of an HF-present model.

This script reconciles by SPLITTING the full-catalog output against the existing
canonical universe (hf_oracle + the re-cased models_dev + core):

  * COLLISION (case-insensitive id already exists as a canonical): emit an
    ALIAS-ONLY enrichment record `{id: <existing-cased-id>, aliases: [...]}`
    so every models.dev provider spelling (carrying its inference_platform in
    metadata.alias_platforms) unions onto the existing HF/curated canonical.
    No duplicate canonical is minted; HF casing is preserved.

  * FRESH (no case-insensitive collision): a genuinely models.dev-only model
    (closed-API families Claude/GPT/Gemini/Grok + the re-host tail) that is not
    on HF — mint it as a NEW canonical.

Output: `seed/models/sources/models_dev_catalog.generated.yaml` (a NEW, distinct
sources file). The re-cased `models_dev.generated.yaml` is left UNTOUCHED, so its
HF casing reproduces identically. The loader merges all `sources/*.generated.yaml`
field-level (aliases UNION), so the enrichment aliases attach cleanly.

Guarantees: no case-insensitive duplicate canonical ids after seed; HF re-casing
intact; closed-API not-on-HF models newly resolvable.

OFFLINE: drives the recovered generator over `/tmp/modelsdev_api.json` (cached);
no network. Run from the registry root:
    LOCAL_MODE=true uv run python scripts/integrate_modelsdev_catalog.py
"""
from __future__ import annotations

import importlib.util
import json
from pathlib import Path

import yaml

from eval_entity_resolver.normalization import normalize

REPO_ROOT = Path(__file__).resolve().parent.parent
SOURCES_DIR = REPO_ROOT / "seed" / "models" / "sources"
HF_ORACLE = SOURCES_DIR / "hf_oracle.generated.yaml"
MODELS_DEV = SOURCES_DIR / "models_dev.generated.yaml"
HUB_STATS = SOURCES_DIR / "hub_stats.generated.yaml"
CORE = REPO_ROOT / "seed" / "models" / "core.yaml"
ENRICH_ALIASES = REPO_ROOT / "seed" / "models" / "enrichments" / "aliases.yaml"
OUT_PATH = SOURCES_DIR / "models_dev_catalog.generated.yaml"
ORGS_YAML = REPO_ROOT / "seed" / "orgs.yaml"
ORGS_GENERATED = REPO_ROOT / "seed" / "orgs.generated.yaml"

# All EXISTING model sources whose id+alias surface forms the catalog must not
# clash with (hf_oracle + the re-cased models_dev + hub_stats + curated
# core + enrichment aliases). HF/curated WIN id+casing.
_EXISTING_SOURCES = (HF_ORACLE, MODELS_DEV, HUB_STATS, CORE, ENRICH_ALIASES)

_HEADER = """# AUTO-GENERATED by scripts/integrate_modelsdev_catalog.py — DO NOT HAND-EDIT.
# HF-aware models.dev full-catalog seed. Two record kinds:
#   * fresh canonical mints: models.dev-only (not-on-HF) models — closed-API
#     families (Claude/GPT/Gemini/Grok) + the re-host/community tail. These have
#     no HF collision, so HF source-of-truth is not violated.
#   * alias-only enrichments {id, aliases}: a models.dev model that IS HF-present
#     (already a canonical). The existing HF-cased canonical wins; only
#     the provider-spelling aliases (carrying inference_platform in
#     metadata.alias_platforms) union onto it. No duplicate canonical is minted.
# The re-cased seed/models/sources/models_dev.generated.yaml is left intact;
# this file is purely additive. Regenerate by re-running the integration script
# (offline, from /tmp/modelsdev_api.json).
"""


def _load_list(path: Path) -> list[dict]:
    if not path.exists():
        return []
    d = yaml.safe_load(path.read_text())
    if isinstance(d, dict):
        d = d.get("entries", [])
    return d or []


def _load_generator():
    path = REPO_ROOT / "scripts" / "refresh_from_modelsdev.py"
    spec = importlib.util.spec_from_file_location("refresh_from_modelsdev", path)
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod


def main() -> int:
    """Thin wrapper: the HF-aware split/dedup + org de-orphan now lives in the
    shared `scripts/refresh_from_modelsdev.py:regenerate_catalog` (the daily cron
    regenerates the catalog via `refresh_from_modelsdev.py --catalog`, so the
    logic must be single-sourced there). This one-shot is kept as a manual
    entry point and simply delegates so the logic stays single-sourced."""
    api_cache = Path("/tmp/modelsdev_api.json")
    if not api_cache.exists():
        print(f"ERROR: offline cache missing at {api_cache}")
        return 1

    mod = _load_generator()
    api = json.loads(api_cache.read_text())
    known_orgs = mod._load_known_org_ids()

    raw_entries, skipped = mod._generate_models(api, known_orgs)
    if skipped:
        print(f"ERROR: {len(skipped)} provider(s) mapped to unknown org_id: {skipped[:5]}")
        return 1
    mod.regenerate_catalog(mod._finalize_entries([dict(e) for e in raw_entries]))
    return 0



if __name__ == "__main__":
    raise SystemExit(main())
